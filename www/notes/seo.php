<h3>Site organisation</h3>
<ul>
	<li>
		<p><strong>&lt;title&gt;</strong> tags for every page (this appears in the search results title on google).  Should be unique, descriptive and not too long.</p>
	</li>
	<li>
		<p>&lt;meta name="description" content="_________"&gt; <strong>Description tags</strong> for every page, unique per page (this appears in the search results body in google).  156 characters max</p>
	</li>
	<li>
		<p><strong>URL's</strong>, easy to understand. Should work if part of it is removed.  e.g. /services/software-development becomes /services/</p>
	</li>
	<li>
		<p>use text / href to <strong>link</strong>, not js.  Make the text descriptive and concise.  Make links clearly visible.</p>
	</li>
	<li>
		<p>put a <strong>site map</strong> on the site</p>
	</li>
	<li>
		<p>Have a useful <strong>404</strong>, should guide users back to content</p>
	</li>
	<li>
		<p><strong>Images</strong> use Alt tags. keep them in a specialized directory.  Use descriptive and concise file names.  Use an image sitemap.</p>
	</li>
	<li>
		<p>If you have <strong>comments</strong>, automate rel="nofollow" on links in them to combat comment spam.  Don't use it for normal links.</p>
	</li>
	<li>
		<p>put a <strong>mobile site map</strong> on the site (if using a mobile site)</p>
	</li>
</ul>
<h3>Content</h3>
<ul>
	<li>
		<p>Create high <strong>quality</strong> content, make it <strong>new</strong>, don't stuff key phrases.  Use a <strong>variety of niche terminology</strong> (write with theme words not key words, eg a variety).  Use <strong>H tags</strong>, use them sparingly.</p>
	</li>
	<li>
		<p>Do not duplicate content</p>
	</li>
	<li>
		<p>use <strong>categories and tags</strong> (don't stuff) &lt; individual pages for each showing related content. Make tags specific and categories general.</p>
	</li>
	<li>
		<p>Create a terminology reference page/index</p>
	</li>
	<li>
		<p>Break up content into clearly defined sections.</p>
	</li>
</ul>
<h3>Backlinks</h3>
<ul>
	<li>
		<p>Participate in <strong>forums</strong>, give value.</p>
	</li>
	<li>
		<p>Write <strong>guest articles</strong> for other sites (don't use the same article on more than one).  Link to your own site in the body of these articles but make it a valuable link.</p>
	</li>
	<li>
		<p>If posting <strong>videos</strong>, use youtube &amp; transcribe content into description with links.</p>
	</li>
	<li>
		<p>You can automate <strong>twitter</strong> but make sure you're posting actual tweets as well, only posting auto tweets will start to look spammy.  Same for <strong>Facebook</strong>.</p>
	</li>
	<li>
		<p>You can get listed on <strong>directories</strong> but hand pick the best ones.</p>
	</li>
	<li>
		<p>Quality of links counts far more than quantity.</p>
	</li>
	<li>
		<p>Do not automate backlink gathering</p>
	</li>
</ul>
<h3>Tools</h3>
<ul>
	<li>
		<p>add business to <strong>google places</strong></p>
	</li>
	<li>
		<p>Use <strong>google webmaster tools, analytics and google website optimizer</strong></p>
	</li>
	<li>
		<p><strong>Majestic SEO</strong> &lt; shows which sites link to you, spammy sites linking to you will hurt your rating</p>
	</li>
</ul>
