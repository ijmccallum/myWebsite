<p><a href="http://bgr.com/2013/07/09/google-search-results-advertisements/">a good point</a> about the diminishing returns of organic results in Google, there are more than a few articles talking about the increased importance of Social Media Optimisation.  There is of course no reason to abandon SEO, just good reason to think of it as a parallel to SMO, possibly even as a little brother to SMO.</p>

